// 1. Arithmetic operators
//     - Unary (one operand) ex : ++ -- 
//     - Binary (two operands) ex : + - * / %

//     Precendence Order - (associativity : left to right)
//     {* , / , %} > {+ , -}

// 2. Comparison operators
//     they give answer either true or false i.e. 0 or 1
//     associativity : left to right in any expression
//     ex : == , > , < , >= , <= , !=

// 3. Logical operators
//     they give answer either true or false i.e. 0 or 1
//     ex : && , || , !



